-- SCENE LOADING --

function route1.load()
    route1.backgroundMusic:setVolume(0.15)                                           -- Sets the Scene's background music volume to 15%
    love.audio.play(route1.backgroundMusic)

    mainCamera = route1.loadCamera(camera.create(0, 0, (WIDTH * 0.55), (WIDTH * 0.45) - ((player.width / 2) ), (HEIGHT * 0.55), (HEIGHT * 0.45), 1920, 0, 1080, 0))
    route1.activeCamera = mainCamera

    if (previousScene == clearMeadowTown) then
        player.physics.body:setY(100)                   -- Sets the Player's physics body's y-coordinate
    end

    for row = 1, #route1.tilemap do
        for col = 1, #route1.tilemap[row] do
            local tileValue = route1.tilemap[row][col]
            if (tileValue ~= 0) then
                route1.loadObject(objectHandler.duplicate(assets[tileValue], ((col - 1) * 80), ((row - 1) * 60)))
            end
        end
    end

    route1.loadObject(objectHandler.create("hill", 0, 1080, 2816, 1, 1, nil, nil, 0, 1080, 2816, 1, "static", 1, 0))
end

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

-- SCENE UPDATING --

function route1.update(dt) 
    if (player.y + player.height < 0) then
        previousScene = route1
        nextScene = clearMeadowTown
    end

    route1.activeCamera.follow(player)

    route1.bottomHalfUnderPlayerTorso = {}
    route1.topHalfUnderPlayerTorso = {}

    route1.bottomHalfUnderPlayerHead = {}
    route1.topHalfUnderPlayerHead = {}

    route1.bottomHalfAbovePlayer = {}
    route1.topHalfAbovePlayer = {}

    player:update(dt)                                       -- Updates the Player's state

    route1.updateObjects(dt)
end

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

-- SCENE DRAWING -- 

function route1.draw()
    route1.activeCamera.draw()
    love.graphics.draw(route1.background, route1.x, route1.y)

    for i = 1, #route1.bottomHalfUnderPlayerTorso do
        route1.bottomHalfUnderPlayerTorso[i]:drawBottom()
    end

    for i = 1, #route1.topHalfUnderPlayerTorso do
        route1.topHalfUnderPlayerTorso[i]:drawTop()
    end

    player:drawBottom()

    for i = 1, #route1.bottomHalfUnderPlayerHead do
        route1.bottomHalfUnderPlayerHead[i]:drawBottom()
    end

    for i = 1, #route1.topHalfUnderPlayerHead do
        route1.topHalfUnderPlayerHead[i]:drawTop()
    end

    player:drawTop()

    for i = 1, #route1.bottomHalfAbovePlayer do
        route1.bottomHalfAbovePlayer[i]:drawBottom()
    end

    for i = 1, #route1.topHalfAbovePlayer do
        route1.topHalfAbovePlayer[i]:drawTop()
    end
end
